package com.example.myfirstapp.entity;

public class Man {
    //人的名称
    private String name;
    //人的年龄
    private int age;
    //人的身高
    private int tall;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getTall() {
        return tall;
    }

    public void setTall(int tall) {
        this.tall = tall;
    }
}